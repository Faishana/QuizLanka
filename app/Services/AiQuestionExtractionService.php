<?php

namespace App\Services;

class AiTextCleaningService
{
    public function clean($text)
    {
        /*
        |--------------------------------------------------------------------------
        | Empty Check
        |--------------------------------------------------------------------------
        */

        if (!$text) {

            return null;
        }

        /*
        |--------------------------------------------------------------------------
        | UTF-8 Normalize
        |--------------------------------------------------------------------------
        */

        $text = mb_convert_encoding(
            $text,
            'UTF-8',
            'UTF-8'
        );

        $text = iconv(
            'UTF-8',
            'UTF-8//IGNORE',
            $text
        );

        /*
        |--------------------------------------------------------------------------
        | Remove Invisible Characters
        |--------------------------------------------------------------------------
        */

        $text = preg_replace(
            '/[\x00-\x1F\x7F]/u',
            ' ',
            $text
        );

        /*
        |--------------------------------------------------------------------------
        | Remove Weird OCR Symbols
        |--------------------------------------------------------------------------
        */

        $text = preg_replace(
            '/[\x00-\x08\x0B\x0C\x0E-\x1F\x7F]/u',
            '',
            $text
        );

        /*
        |--------------------------------------------------------------------------
        | Normalize Spaces
        |--------------------------------------------------------------------------
        */

        $text = preg_replace(
            '/[ \t]+/u',
            ' ',
            $text
        );

        /*
        |--------------------------------------------------------------------------
        | Collapse Empty Lines
        |--------------------------------------------------------------------------
        */

        $text = preg_replace(
            "/\n{3,}/u",
            "\n\n",
            $text
        );

        /*
        |--------------------------------------------------------------------------
        | Trim Lines
        |--------------------------------------------------------------------------
        */

        $lines = explode("\n", $text);

        $lines = array_map(function ($line) {

            return trim($line);

        }, $lines);

        $text = implode("\n", $lines);

        /*
        |--------------------------------------------------------------------------
        | Final Trim
        |--------------------------------------------------------------------------
        */

        return trim($text);
    }
}
